public class StringExercise {
    public static void main(String[] args) {
        // 1. String Basics
        String education = "We don't need no education";
        System.out.println("education = " + education);

        String control = "We don't need no thought control";
        System.out.println("control = " + control);

        String checkIt = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println("checkIt = " + checkIt);

        String drive = "In windows, the main drive is usually C:\\";
        System.out.println("drive = " + drive);

        String nextNonsense = "I can do backslashes\\, double backslahses \\\\,\n and the amazing triple backslash \\\\\\";
        System.out.println("nextNonsense = " + nextNonsense);

    }
}
